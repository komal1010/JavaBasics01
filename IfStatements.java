public class IfStatements {
    public static void main(String[] args) {
        int age = 67;

        if(age<=30) {
            System.out.println("You are young");
        }
        else if(age<=50) {
            System.out.println("You are middle-aged");
        }
        else if (age<=70){
                System.out.println("You are seventies");
            }
        else{
            System.out.println("You are old");
        }
    }
}
    
